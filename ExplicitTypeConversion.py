# Demonstration of Explicit Type Conversion in Python

a = '4'

# printing and converting a character to an integer

b = ord(a)

print("After converting character into integer : ")
print(b)

# printing integer converting to hexadecimal string


b = hex(56)

# printing hexadecimal value and converting into a octal

print("After converting integer to hexadecimal")

print(b)

b = oct(56)

# printing a octal number

print("After Converting HexaDecimal to Octal : ")

print(b)
